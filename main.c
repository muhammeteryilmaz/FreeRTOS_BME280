void BME280_Read_Calibration() {
    uint8_t calib_data[26];
    HAL_I2C_Mem_Read(&hi2c1, BME280_I2C_ADDRESS, BME280_CALIB_DATA_START, 1, calib_data, 26, HAL_MAX_DELAY);

    dig_T1 = (calib_data[1] << 8) | calib_data[0];
    dig_T2 = (calib_data[3] << 8) | calib_data[2];
    dig_T3 = (calib_data[5] << 8) | calib_data[4];
}

void vBME280_Compensate_Temperature(void *pvParameters){
    int32_t var1, var2, T;
    uint8_t data[8];

    BaseType_t xStatus;

    for(;;){

        HAL_I2C_Mem_Read(&hi2c1, BME280_I2C_ADDRESS, BME280_REG_PRESS_MSB, 1, data, 8, HAL_MAX_DELAY);

        int32_t adc_T = ((uint32_t)data[3] << 12) | ((uint32_t)data[4] << 4) | ((uint32_t)data[5] >> 4);

        var1 = ((((adc_T >> 3) - ((int32_t)dig_T1 << 1))) * ((int32_t)dig_T2)) >> 11;
        var2 = (((((adc_T >> 4) - ((int32_t)dig_T1)) * ((adc_T >> 4) - ((int32_t)dig_T1))) >> 12) * ((int32_t)dig_T3)) >> 14;
        T = (((var1 + var2) * 5) + 128) >> 8;

    	xStatus = xQueueSendToBack(xQueue, &T, 0);
    	sender = 1;
    	receiver = 0;
    	if(xStatus != pdPASS){
    		senderCheck = 0;
    	}else{
    		senderCheck = 1;
    	}

    }
}

void vBME280_Read_Data(void *pvParameters) {



    int32_t lReceivedValue;
    BaseType_t xStatus;

    const TickType_t xTicksToWait = pdMS_TO_TICKS( 100 );



    for (;;){

        if(uxQueueMessagesWaiting(xQueue) != 0){
        	messageWaiting = 1;
        }

        xStatus = xQueueReceive(xQueue, &lReceivedValue, xTicksToWait);
    	sender = 0;
    	receiver = 1;

        if(xStatus == pdPASS){
        	receiverCheck = 1;
        	temp = (float)lReceivedValue / 100;
        }
        else{
        	receiverCheck = 0;
        }
    }
}




void BME280_Init() {
    uint8_t id;
    HAL_I2C_Mem_Read(&hi2c1, BME280_I2C_ADDRESS, BME280_REG_ID, 1, &id, 1, HAL_MAX_DELAY);
    if (id != 0x60) {
        printf("BME280 Could not find!\n");
    }

    uint8_t reset_cmd = 0xB6;
    HAL_I2C_Mem_Write(&hi2c1, BME280_I2C_ADDRESS, BME280_REG_RESET, 1, &reset_cmd, 1, HAL_MAX_DELAY);
    HAL_Delay(10);

    uint8_t filter = 0xA4;
    HAL_I2C_Mem_Write(&hi2c1, BME280_I2C_ADDRESS, BME280_REG_CONFIG, 1, &filter, 1, HAL_MAX_DELAY);

    BME280_Read_Calibration();



    uint8_t ctrl_meas = 0x67;
    HAL_I2C_Mem_Write(&hi2c1, BME280_I2C_ADDRESS, BME280_REG_CTRL_MEAS, 1, &ctrl_meas, 1, HAL_MAX_DELAY);
}



